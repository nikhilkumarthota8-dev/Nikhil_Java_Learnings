package strings;

public class StringsReplace {
    public static void main(String[] args) {
        String str = "Thota Nikhil  Kumar  ";
        System.out.println("Trim:" + str.trim());
        System.out.println("Replace:" + str.replace(" ", ""));
        System.out.println("Replace:" + str.replace("  ", ""));
    }
}
