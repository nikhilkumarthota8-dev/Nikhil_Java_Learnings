package test_folder_practise.otwani;

public class TableDecrement {
    public static void main(String[] args) {
        int number = 20;
        System.out.println(number);
        for (int i = 10; i >= 1; i--) {
            System.out.println(number + "X" + i + "=" + (number * i));
        }
    }
}
