public class LargestNumberAmongTwoNumbers {
    public static void main(String[] args) {
        int n1 = 10;
        int n2 = 20;

        if (n1 > n2) {
            System.out.println("N1 is larger");
        } else {
            System.out.println("N2 is larger");
        }
    }
}
